public class Wall {
    public float x;
    public float y;
    public float z;

    public float scaleX;
    public float scaleY;
    public float ScaleZ;

    Wall(float x, float y, float z, float scaleX, float scaleY, float scaleZ){
        this.x = x;
        this.y = y;
        this.z = z;
        this.scaleX = scaleX;
        this.scaleY = scaleY;
        this.ScaleZ = scaleZ;
    }
}
